package ru.tochilinmi;

public class Main {

    public static void main(String[] args) {
        int number = 10;
        number = 11;
        System.out.println("number: "+number);

        Long long_num = 10000000L;
        System.out.println("big_num: "+long_num);

        float float_num = 5.76f;
        System.out.println("float_num: "+float_num);

        double double_num = 5.76156155d;
        System.out.println("double_num: "+double_num);

        boolean haveCar = true;
        System.out.println("haveCar: "+haveCar);

        char char_a = 'a';
        System.out.println("char_a:"+char_a);

        String str = "Hello world!";
        System.out.println(str);

    }
}
